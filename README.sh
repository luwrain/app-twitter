
# Twitter for LUWRAIN

This repo doesn't include the consumer key and consumer secret.
The corresponding places in the source code where these values must be  used
marked   with the ```luwrain-twitter-consumer-key``` and ```luwrain-twitter-consumer-secret``` strings.
We substitute these values on compilation.
if you would like to use this code,
you have to obtain new values from Twitter  using [this page](https://developer.twitter.com/apps).

LUWRAIN is a platform for the creation of apps for the blind and partially-sighted.
It provides a Java API for constructing highly-distributable apps
with the user interface adjusted to the perception of blind people.
Please visit [luwrain.org](http://luwrain.org/?lang=en) for further information.
